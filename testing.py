# 1. Simple text output
print("Hello, world!")

# 2. Printing multiple items
print("The answer is", 42)

# 3. String concatenation
print("Python " + "is fun!")

# 4. Using f-strings for formatting
name = "Alice"
print(f"Hello, {name}!")

# 5. Printing numbers and arithmetic results
print("2 + 3 =", 2 + 3)

# 6. Printing with a custom separator
print("apple", "banana", "cherry", sep=" | ")

# 7. Printing without a newline at the end
print("This is on one line", end=" ")
print("and this continues it.")

# 8. Printing lists or other data structures
fruits = ["apple", "banana", "cherry"]
print(fruits)

# 9. Printing with escape characters
print("First line\nSecond line\t(Indented)")

# 10. Printing using format()
language = "Python"
version = 3.11
print("I'm learning {} version {}!".format(language, version))
